def ajouter_melon():
    # Définition de la liste de fruits
    fruits = ["pomme", "cerise", "orange"]
    
    # Ajout de "Melon" à la fin de la liste
    fruits.append("Melon")
    
    # Affichage du résultat
    print("Liste de fruits après ajout de Melon :", fruits)

# Appel de la fonction
ajouter_melon()